    private boolean verificar() {
        return !(jTextFieldUsuario.getText().trim().isEmpty() | jPasswordFieldSenha.getText().trim().isEmpty());
    }

    private Cadastro criar() {
        Cadastro usuario;
        usuario = new Cadastro();

        usuario.setLogin(jTextFieldUsuario.getText());
        usuario.setSenha(jPasswordFieldSenha.getText());

        return usuario;
    }
