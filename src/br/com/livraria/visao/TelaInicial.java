    private void jButtonAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAcessarActionPerformed
            if(verificar() == false){
                JOptionPane.showConfirmDialog(null,"Campo vazio");
            }else{
                String campoSenha = new String(jPasswordFieldSenha.getPassword());
                CadastroDao usuario = null;
                
                try{
                    usuario = new CadastroDaoArquivo();
                    Cadastro logado = usuario.acessar(jTextFieldUsuario.getText(),campoSenha);
                    if(logado != null){
                        TelaDeFuncionalidade proxTela = new TelaDeFuncionalidade();
                        this.setLocationRelativeTo(null);
                        proxTela.setVisible(true);
                        dispose();
                    }else{
                        JOptionPane.showMessageDialog(null, "Usuário ou senha incorreto!" + " Ou usuário não está cadastrado!!!");
                    }
                }catch (IOException | ClassNotFoundException ex){
                    Logger.getLogger(TelaInicial.class).log(Level.SEVERE, null, ex);
                    
                }
                
                
                
            }
            
        
    }//GEN-LAST:event_jButtonAcessarActionPerformed

    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
      
        Cadastro u = new Cadastro(jTextFieldUsuario.getText(),jPasswordFieldSenha.getText());
            
        try{
            if(dao.salvar(u)){
                JOptionPane.showMessageDialog(null,"Usuário cadastrado com sucesso!");
            }else{
                JOptionPane.showMessageDialog(null, "Falha ao casdastrar usuário!",
                        "Mensagem de erro", JOptionPane.ERROR_MESSAGE);
            }
        }catch(IOException | ClassNotFoundException ex){
            JOptionPane.showMessageDialog(null, "Falha na conexão com o arquivo",
                    "Mensagem de erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

